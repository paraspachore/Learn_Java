package Assignments;

public class Assignment1 {

	public static void main(String[] args) {
		// Program to swap to numbers
		int a=10;
		int b=20;
		
		System.out.println("given values of a & b : "+a+" and "+b);
		
		int temp;
		temp=a;
		a=b;
		b=temp;
		System.out.println("swapped values of a & b : "+a+" and "+b);
	}

}
